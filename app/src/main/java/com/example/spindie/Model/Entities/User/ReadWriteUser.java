    public void writeNewUserRealtime(User user) {
        Map<String, User> users = new HashMap<>();
        users.put(user.getUserNick(), new User(user.getUserName(), user.getUserEmail(), user.getUserId()));
        usersRef.setValue(users);
    }

    public void writeNewUser(User user) {
        // Create a new user with a first and last name
        Map<String, Object> newUser = new HashMap<>();
        newUser.put("name", user.getUserName());
        newUser.put("email", user.getUserEmail());
        newUser.put("id", user.getUserId());

        // Add a new document with a generated ID
        db.collection("users")
                .add(user)
                .addOnSuccessListener(new OnSuccessListener<DocumentReference>() {
                    @Override
                    public void onSuccess(DocumentReference documentReference) {
                        Log.d(TAG, "DocumentSnapshot added with ID: " + documentReference.getId());
                    }
                })
                .addOnFailureListener(new OnFailureListener() {
                    @Override
                    public void onFailure(@NonNull Exception e) {
                        Log.w(TAG, "Error adding document", e);
                    }
                });

    }

    public void getAllUsers() {
        ArrayList<User> userList= new ArrayList<User>();
        db.collection("users")
                .get()
                .addOnCompleteListener(new OnCompleteListener<QuerySnapshot>() {
                    @Override
                    public void onComplete(@NonNull Task<QuerySnapshot> task) {
                        if (task.isSuccessful()) {
                            for (QueryDocumentSnapshot document : task.getResult()) {
                                Log.d(TAG, document.getId() + " => " + document.getData());
                                Map<String, Object> usersMap = document.getData();
                            }
                        } else {
                            Log.w(TAG, "Error getting documents.", task.getException());
                        }
                    }
                });
    }

